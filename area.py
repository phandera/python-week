""" area module written by michael """
from math import pi 
def square(a):
    """ square area function """
    #square
    print(a*a)
    
def circle(r):
    """ circle area function """
    #circle
    print(pi*r*r)
    
def triangle(b,h):
    """ triangle area function """
    #traingle
    print(1/2*b*h)

def trapezium(a,b,h):
    """ trapezium area function """
    #trapezium
    print(1/2*(a+b)*h)
    
def rectangle(l,w):
    """ rectangle area function """
    #rectangle
    print(l*w)


